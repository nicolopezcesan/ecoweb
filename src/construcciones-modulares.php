<h2 style="padding-top: 25px;">
Sistema de Construcción Modular
</h2>

<p>
Estas construcciones móviles y modulares tienen la singularidad de ser velozmente desmontables, lo cual implica que se pueden armar en un sitio, y luego desmotar rápidamente para armarse en otro lugar, aprovechando prácticamente la totalidad de los materiales (el 95% para ser más precisos).
Con costos de traslado realmente muy bajos –ya que es factible mover hasta 150 metros cuadrados con un único camión-, estas construcciones modulares prefabricadas, además de tener precios iguales o menores a la construcción tradicional, dan lugar a soluciones con una óptima relación costo-beneficio, ya que además se pueden reutilizar.  
</p>

<p>
Esta versatilidad, hizo que las construcciones modulares en Argentina tengan ya un altísimo nivel de aceptación en proyectos de ingeniería  o minería, por caso, o para desarrollos de Vialidad. También para proyectos educativos rurales este sistema de construcción modular puede ofrecer soluciones muy apropiadas. O para el desarrollo de salas de atención primaria de la salud en diferentes pueblos y localidades.
</p>

