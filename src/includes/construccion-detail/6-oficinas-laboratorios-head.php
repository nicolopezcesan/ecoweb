            <br><h3 class="text-dark"><strong>CONCEPCIÓN BIM (BIM, BUILDING INFORMATION MODELLING)</strong></h3>
            <p>La ejecución del proyecto, documentación ejecutiva, programación de la obra, y ejecución de la misma se realiza bajo la concepción BIM (BIM, Building Information Modelling) incluyendo entre otros: documentación ejecutada en REVIT, BIM 360 Collaborate Pro, seguimiento de obra a través de la Plataforma BUILD. <br>
              El concepto BIM integral con el cual ECOSAN desarrolla y ejecuta todas sus obras permite minimizar los errores, detectar interferencias, tener un seguimiento de la obra en tiempo real, permitiendo así poder desarrollar proyectos y obras de extrema complejidad en un plazo muy breve, con el menor nivel de error posible.</p>
            
